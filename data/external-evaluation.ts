export function getExternalEvaluationDomain(domainId: string) {
  return externalEvaluationDomains.find((domain) => domain.id === domainId) || null;
}

export function getAllExternalEvaluationIndicators() {
  return externalEvaluationDomains.flatMap((domain) =>
    domain.standards.flatMap((standard) =>
      standard.indicators.map((indicator) => ({
        domainId: domain.id,
        domainTitle: domain.title,
        standardId: standard.id,
        standardTitle: standard.title,
        indicatorCode: indicator.code,
        indicatorText: indicator.text,
        tools: indicator.tools ?? [...externalEvaluationDefaultTools],
        guidance: indicator.guidance,
      })),
    ),
  );
}
